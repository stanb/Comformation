using System;
using System.Collections.Generic;
using Newtonsoft.Json;
using Comformation.IntrinsicFunctions;

namespace Comformation.DataBrew.Job
{
    /// <summary>
    /// AWS::DataBrew::Job DatabaseOutput
    /// https://docs.aws.amazon.com/AWSCloudFormation/latest/UserGuide/aws-properties-databrew-job-databaseoutput.html
    /// </summary>
    public class DatabaseOutput
    {

        /// <summary>
        /// GlueConnectionName
        /// The AWS Glue connection that stores the connection information for the target database.
        /// Required: Yes
        /// Type: String
        /// Minimum: 1
        /// Maximum: 255
        /// Update requires: No interruption
        /// </summary>
        [JsonProperty("GlueConnectionName")]
        public Union<string, IntrinsicFunction> GlueConnectionName { get; set; }

        /// <summary>
        /// DatabaseOutputMode
        /// The output mode to write into the database. Currently supported option: NEW_TABLE.
        /// Required: No
        /// Type: String
        /// Allowed values: NEW_TABLE
        /// Update requires: No interruption
        /// </summary>
        [JsonProperty("DatabaseOutputMode")]
        public Union<string, IntrinsicFunction> DatabaseOutputMode { get; set; }

        /// <summary>
        /// DatabaseOptions
        /// Represents options that specify how and where DataBrew writes the database output generated by
        /// recipe jobs.
        /// Required: Yes
        /// Type: DatabaseTableOutputOptions
        /// Update requires: No interruption
        /// </summary>
        [JsonProperty("DatabaseOptions")]
        public DatabaseTableOutputOptions DatabaseOptions { get; set; }

    }
}
