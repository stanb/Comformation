using System;
using System.Collections.Generic;
using Comformation.IntrinsicFunctions;

namespace Comformation.GuardDuty.ThreatIntelSet
{
    /// <summary>
    /// AWS::GuardDuty::ThreatIntelSet
    /// The AWS::GuardDuty::ThreatIntelSet resource specifies a new ThreatIntelSet. A ThreatIntelSet consists of known
    /// malicious IP addresses. GuardDuty generates findings based on the ThreatIntelSet when it is activated.
    /// https://docs.aws.amazon.com/AWSCloudFormation/latest/UserGuide/aws-resource-guardduty-threatintelset.html
    /// </summary>
    public class ThreatIntelSetResource : ResourceBase
    {
        public class ThreatIntelSetProperties
        {
            /// <summary>
            /// Format
            /// The format of the file that contains the ThreatIntelSet.
            /// Required: Yes
            /// Type: String
            /// Update requires: Replacement
            /// </summary>
			public Union<string, IntrinsicFunction> Format { get; set; }

            /// <summary>
            /// Activate
            /// Specifies whether or not GuardDuty uses the ThreatIntelSet.
            /// Required: Yes
            /// Type: Boolean
            /// Update requires: No interruption
            /// </summary>
			public Union<bool, IntrinsicFunction> Activate { get; set; }

            /// <summary>
            /// DetectorId
            /// The ID of the detector to associate the ThreatIntelSet with.
            /// Required: Yes
            /// Type: String
            /// Update requires: Replacement
            /// </summary>
			public Union<string, IntrinsicFunction> DetectorId { get; set; }

            /// <summary>
            /// Name
            /// A name for the ThreatIntelSet. The name is displayed in all finding generated by activity associated
            /// with the IP addresses included in this ThreatIntelSet.
            /// Required: No
            /// Type: String
            /// Update requires: No interruption
            /// </summary>
			public Union<string, IntrinsicFunction> Name { get; set; }

            /// <summary>
            /// Location
            /// The URI of the file that contains the ThreatIntelSet.
            /// Required: Yes
            /// Type: String
            /// Update requires: No interruption
            /// </summary>
			public Union<string, IntrinsicFunction> Location { get; set; }

        }

        public string Type { get; } = "AWS::GuardDuty::ThreatIntelSet";

        public ThreatIntelSetProperties Properties { get; } = new ThreatIntelSetProperties();

    }
}
