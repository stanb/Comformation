using System;
using System.Collections.Generic;
using Comformation.IntrinsicFunctions;

namespace Comformation.DMS.EventSubscription
{
    /// <summary>
    /// AWS::DMS::EventSubscription
    /// Use the AWS::DMS::EventSubscription resource to get notifications for AWS Database Migration Service events
    /// through the Amazon Simple Notification Service. For more information, see Using AWS DMS Event Notification in
    /// the AWS Database Migration Service User Guide.
    /// https://docs.aws.amazon.com/AWSCloudFormation/latest/UserGuide/aws-resource-dms-eventsubscription.html
    /// </summary>
    public class EventSubscriptionResource : ResourceBase
    {
        public class EventSubscriptionProperties
        {
            /// <summary>
            /// SourceType
            /// The type of source for which AWS DMS provides notification events. For example, if you want to be
            /// notified of events generated by a database instance, set this parameter to replication-instance. If
            /// you don&#39;t specify a value, notifications are provided for all source types. For valid values, see
            /// the SourceType parameter for the CreateEventSubscription action in the AWS Database Migration
            /// Service API Reference.
            /// Required: Conditional. If you specify the SourceIds or EventCategories property, you must specify
            /// this property.
            /// Type: String
            /// Update requires: No interruption
            /// </summary>
			public Union<string, IntrinsicFunction> SourceType { get; set; }

            /// <summary>
            /// EventCategories
            /// A list of event categories that you want to subscribe to for a given source type. If you don&#39;t
            /// specify this property, you are notified about all event categories. For more information, see Using
            /// AWS DMS Event Notification in the AWS Database Migration Service User Guide.
            /// Required: No
            /// Type: List of String values
            /// Update requires: No interruption
            /// </summary>
			public List<Union<string, IntrinsicFunction>> EventCategories { get; set; }

            /// <summary>
            /// Enabled
            /// Indicates whether to activate the subscription. If you don&#39;t specify this property, AWS
            /// CloudFormation activates the subscription.
            /// Required: No
            /// Type: Boolean
            /// Update requires: No interruption
            /// </summary>
			public Union<bool, IntrinsicFunction> Enabled { get; set; }

            /// <summary>
            /// SubscriptionName
            /// The subscription name.
            /// If you don&#39;t specify a value, we create a random value.
            /// Required: No
            /// Type: List of String values
            /// Update requires: Replacement
            /// </summary>
			public Union<string, IntrinsicFunction> SubscriptionName { get; set; }

            /// <summary>
            /// SnsTopicArn
            /// The Amazon Resource Name (ARN) of an Amazon SNS topic that you want to send event notifications to.
            /// Required: Yes
            /// Type: String
            /// Update requires: No interruption
            /// </summary>
			public Union<string, IntrinsicFunction> SnsTopicArn { get; set; }

            /// <summary>
            /// SourceIds
            /// A list of identifiers for which AWS DMS provides notification events.
            /// If you don&#39;t specify a value, notifications are provided for all sources. If you specify multiple
            /// values, they must be of the same type. For example, if you specify a database instance ID, all other
            /// values must be database instance IDs.
            /// Required: No
            /// Type: List of String values
            /// Update requires: Replacement
            /// </summary>
			public List<Union<string, IntrinsicFunction>> SourceIds { get; set; }

            /// <summary>
            /// Tags
            /// The tags that you want to attach to the DMS event subscription.
            /// Required: No
            /// Type: List of resource tags in key-value format
            /// Update requires: Replacement
            /// </summary>
			public List<Tag> Tags { get; set; }

        }

        public string Type { get; } = "AWS::DMS::EventSubscription";

        public EventSubscriptionProperties Properties { get; } = new EventSubscriptionProperties();

    }
}
