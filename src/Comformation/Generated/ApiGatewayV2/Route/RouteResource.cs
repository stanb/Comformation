using System;
using System.Collections.Generic;
using Comformation.IntrinsicFunctions;

namespace Comformation.ApiGatewayV2.Route
{
    /// <summary>
    /// AWS::ApiGatewayV2::Route
    /// https://docs.aws.amazon.com/AWSCloudFormation/latest/UserGuide/aws-resource-apigatewayv2-route.html
    /// </summary>
    public class RouteResource : ResourceBase
    {
        public class RouteProperties
        {
            /// <summary>
            /// Target
            /// The target for the route.
            /// Required: No
            /// Type: String
            /// Update requires: No interruption
            /// </summary>
            public Union<string, IntrinsicFunction> Target { get; set; }

            /// <summary>
            /// RouteResponseSelectionExpression
            /// The route response selection expression for the route. Supported only for WebSocket APIs.
            /// Required: No
            /// Type: String
            /// Update requires: No interruption
            /// </summary>
            public Union<string, IntrinsicFunction> RouteResponseSelectionExpression { get; set; }

            /// <summary>
            /// AuthorizerId
            /// The identifier of the Authorizer resource to be associated with this route. The authorizer
            /// identifier is generated by API Gateway when you created the authorizer.
            /// Required: No
            /// Type: String
            /// Update requires: No interruption
            /// </summary>
            public Union<string, IntrinsicFunction> AuthorizerId { get; set; }

            /// <summary>
            /// RequestModels
            /// The request models for the route. Supported only for WebSocket APIs.
            /// Required: No
            /// Type: Json
            /// Update requires: No interruption
            /// </summary>
            public Union<Newtonsoft.Json.Linq.JToken, IntrinsicFunction> RequestModels { get; set; }

            /// <summary>
            /// OperationName
            /// The operation name for the route.
            /// Required: No
            /// Type: String
            /// Update requires: No interruption
            /// </summary>
            public Union<string, IntrinsicFunction> OperationName { get; set; }

            /// <summary>
            /// AuthorizationScopes
            /// The authorization scopes supported by this route.
            /// Required: No
            /// Type: List of String
            /// Update requires: No interruption
            /// </summary>
            public List<Union<string, IntrinsicFunction>> AuthorizationScopes { get; set; }

            /// <summary>
            /// ApiKeyRequired
            /// Specifies whether an API key is required for the route. Supported only for WebSocket APIs.
            /// Required: No
            /// Type: Boolean
            /// Update requires: No interruption
            /// </summary>
            public Union<bool, IntrinsicFunction> ApiKeyRequired { get; set; }

            /// <summary>
            /// RouteKey
            /// The route key for the route. For HTTP APIs, the route key can be either $default, or a combination
            /// of an HTTP method and resource path, for example, GET /pets.
            /// Required: Yes
            /// Type: String
            /// Update requires: No interruption
            /// </summary>
            public Union<string, IntrinsicFunction> RouteKey { get; set; }

            /// <summary>
            /// AuthorizationType
            /// The authorization type for the route. For WebSocket APIs, valid values are NONE for open access,
            /// AWS_IAM for using AWS IAM permissions, and CUSTOM for using a Lambda authorizer. For HTTP APIs,
            /// valid values are NONE for open access, JWT for using JSON Web Tokens, AWS_IAM for using AWS IAM
            /// permissions, and CUSTOM for using a Lambda authorizer.
            /// Required: No
            /// Type: String
            /// Update requires: No interruption
            /// </summary>
            public Union<string, IntrinsicFunction> AuthorizationType { get; set; }

            /// <summary>
            /// ModelSelectionExpression
            /// The model selection expression for the route. Supported only for WebSocket APIs.
            /// Required: No
            /// Type: String
            /// Update requires: No interruption
            /// </summary>
            public Union<string, IntrinsicFunction> ModelSelectionExpression { get; set; }

            /// <summary>
            /// ApiId
            /// The API identifier.
            /// Required: Yes
            /// Type: String
            /// Update requires: Replacement
            /// </summary>
            public Union<string, IntrinsicFunction> ApiId { get; set; }

            /// <summary>
            /// RequestParameters
            /// The request parameters for the route. Supported only for WebSocket APIs.
            /// Required: No
            /// Type: Json
            /// Update requires: No interruption
            /// </summary>
            public Union<Newtonsoft.Json.Linq.JToken, IntrinsicFunction> RequestParameters { get; set; }

        }

        public string Type { get; } = "AWS::ApiGatewayV2::Route";

        public RouteProperties Properties { get; } = new RouteProperties();

    }
}
