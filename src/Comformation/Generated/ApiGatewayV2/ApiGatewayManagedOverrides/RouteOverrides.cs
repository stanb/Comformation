using System;
using System.Collections.Generic;
using Newtonsoft.Json;
using Comformation.IntrinsicFunctions;

namespace Comformation.ApiGatewayV2.ApiGatewayManagedOverrides
{
    /// <summary>
    /// AWS::ApiGatewayV2::ApiGatewayManagedOverrides RouteOverrides
    /// https://docs.aws.amazon.com/AWSCloudFormation/latest/UserGuide/aws-properties-apigatewayv2-apigatewaymanagedoverrides-routeoverrides.html
    /// </summary>
    public class RouteOverrides
    {

        /// <summary>
        /// Target
        /// For HTTP integrations, specify a fully qualified URL. For Lambda integrations, specify a function
        /// ARN. The type of the integration will be HTTP_PROXY or AWS_PROXY, respectively.
        /// Required: No
        /// Type: String
        /// Update requires: No interruption
        /// </summary>
        [JsonProperty("Target")]
        public Union<string, IntrinsicFunction> Target { get; set; }

        /// <summary>
        /// AuthorizerId
        /// The identifier of the Authorizer resource to be associated with this route. The authorizer
        /// identifier is generated by API Gateway when you created the authorizer.
        /// Required: No
        /// Type: String
        /// Update requires: No interruption
        /// </summary>
        [JsonProperty("AuthorizerId")]
        public Union<string, IntrinsicFunction> AuthorizerId { get; set; }

        /// <summary>
        /// OperationName
        /// The operation name for the route.
        /// Required: No
        /// Type: String
        /// Update requires: No interruption
        /// </summary>
        [JsonProperty("OperationName")]
        public Union<string, IntrinsicFunction> OperationName { get; set; }

        /// <summary>
        /// AuthorizationScopes
        /// The authorization scopes supported by this route.
        /// Required: No
        /// Type: List of String
        /// Update requires: No interruption
        /// </summary>
        [JsonProperty("AuthorizationScopes")]
        public List<Union<string, IntrinsicFunction>> AuthorizationScopes { get; set; }

        /// <summary>
        /// AuthorizationType
        /// The authorization type for the route. To learn more, see AuthorizationType.
        /// Required: No
        /// Type: String
        /// Update requires: No interruption
        /// </summary>
        [JsonProperty("AuthorizationType")]
        public Union<string, IntrinsicFunction> AuthorizationType { get; set; }

    }
}
